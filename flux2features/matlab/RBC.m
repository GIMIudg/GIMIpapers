%% Supplementary Material – Model Generation and Sampling Workflow
% This Live Script documents the generation of disease-constrained RBC-GEM models 
% and the subsequent flux balance and flux sampling analyses. It is intended as 
% supplementary material to ensure reproducibility of the computational experiments.
%% Initialization of the COBRA Toolbox and Solver
% We begin by initializing the COBRA Toolbox and selecting the Gurobi solver, 
% which is used for both flux balance analysis (FBA) and flux sampling procedures.

initCobraToolbox(false)
changeCobraSolver('gurobi', 'all')
%% Loading the RBC-GEM Model
% The reference red blood cell genome-scale model (RBC-GEM) is loaded. A conditional 
% check is included to adapt the working directory depending on the computer hostname, 
% allowing collaborators to specify their own paths.

[~, name] = system('hostname');
